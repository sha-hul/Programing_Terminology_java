class Datatype{
    public static void main(String[] args) {
        int a=20;
        char b='S';
        long c=123231242444L;
        float d=313.42f;
        double e=3245.525252d;
        boolean f=false;
        System.out.println(a+" "+b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}